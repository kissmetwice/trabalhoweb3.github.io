function alterarCor1() {
  const stt = document.getElementById('sttArm1');
  if (stt.style.backgroundColor === 'lightgreen') {
    stt.style.backgroundColor = 'lightcoral';
  } else {
    stt.style.backgroundColor = 'lightgreen';
  }
}

